// scripts/create-cron-entry.mjs
// post-build：產生 dist/cron-entry.js，讓 wrangler 用這個 entry，
// 同時保留 Astro 的 fetch handler 並加入 scheduled handler
// 也更新 dist/server/wrangler.json 指向 cron-entry.js

import { writeFileSync, readFileSync, existsSync } from 'node:fs'

const ASTRO_WORKER = './dist/server/entry.mjs'
const OUTPUT = './dist/cron-entry.js'
const SERVER_WRANGLER = './dist/server/wrangler.json'

if (!existsSync(ASTRO_WORKER)) {
  console.error('Astro worker not found. Run astro build first.')
  process.exit(1)
}

const content = `// Auto-generated by scripts/create-cron-entry.mjs
// Do not edit manually.
import astroWorker from './server/entry.mjs';
import { scheduledAgentEntries } from '../src/lib/agent-os/scheduler/cron-registry.ts';
import { checkKernelHealth } from '../src/lib/agent-os/observability/alerts.ts';
import { handleQueueBatch } from '../src/server/queue.ts';

export default {
  // 保留 Astro 的所有 handlers
  ...astroWorker,

  async queue(batch, env, ctx) {
    return handleQueueBatch(batch, env, ctx)
  },

  // 加入 Cron scheduled handler
  async scheduled(event, env, ctx) {
    const secret = env.CRAWL_SECRET

    if (event.cron === '0 3 * * *') {
      ctx.waitUntil(checkKernelHealth(env))
    }

    const baseUrl = normalizeCronBaseUrl(env.APP_BASE_URL || env.CRAWL_BASE_URL || env.WORKER_URL || env.CF_PAGES_URL || 'https://quidproquo.cc')
    const retentionUrl = baseUrl + '/api/admin/traces/retention'
    const crawlUrl = baseUrl + '/api/crawl/sync'
    const pipelineUrl = baseUrl + '/api/admin/pipelines/scheduled'
    const agentScheduledUrl = baseUrl + '/api/admin/agents/scheduled'

    const isWeeklyCrawl = event.cron === '0 2 * * SUN'
    const scheduledPipelineTasks = {
      '0 4 * * SUN': [
        { pipelineId: 'series-suggestions' },
      ],
      '0 5 * * SUN': [
        { pipelineId: 'knowledge-graph-prototype' },
      ],
    }

    const scheduledPipelines = scheduledPipelineTasks[event.cron] ?? []
    const scheduledAgents = scheduledAgentEntries.filter((entry) => entry.cron === event.cron)
    const tasks = []

    if (!secret) {
      console.error('[cron] CRAWL_SECRET not set, skipping HTTP scheduled jobs')
    } else {
      tasks.push(postWithSecret(retentionUrl, secret, { scope: 'all' }))

      if (isWeeklyCrawl) {
        tasks.push(postWithSecret(crawlUrl, secret))
      }
      for (const task of scheduledPipelines) {
        tasks.push(postWithSecret(pipelineUrl, secret, {
          pipelineId: task.pipelineId,
          source: 'cron',
          cron: event.cron,
        }))
      }
      for (const entry of scheduledAgents) {
        tasks.push(postWithSecret(agentScheduledUrl, secret, {
          agentId: entry.agentId,
          cronExpression: entry.cron,
          input: entry.input ?? {},
        }))
      }
    }

    console.log('[cron] Scheduling maintenance jobs', JSON.stringify({
      cron: event.cron,
      retention: Boolean(secret),
      crawl: isWeeklyCrawl,
      pipelines: scheduledPipelines.map((pipeline) => pipeline.pipelineId),
      agents: scheduledAgents.map((entry) => entry.agentId),
      tasks: tasks.length,
    }))
    if (tasks.length === 0) return

    ctx.waitUntil(
      Promise.all(tasks).catch((err) => {
        console.error('[cron] Maintenance failed:', err)
      })
    )

    async function postWithSecret(url, secretValue, body = {}) {
      try {
        const response = await fetch(url, {
          method: 'POST',
          headers: {
            'X-Crawl-Secret': secretValue,
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(body),
        })
        const payload = await response.text()
        console.log('[cron] Result', url, response.status, payload)
      } catch (err) {
        console.error('[cron] Request failed', url, err)
      }
    }

    function normalizeCronBaseUrl(value) {
      if (!value) return 'https://quidproquo.cc'
      const trimmed = String(value).trim()
      if (!trimmed) return 'https://quidproquo.cc'
      return trimmed.replace(/\\/$/, '')
    }
  },
}
`

writeFileSync(OUTPUT, content)
console.log('✅ Created', OUTPUT)

// Patch dist/server/wrangler.json to point main to cron-entry.js
if (existsSync(SERVER_WRANGLER)) {
  const wranglerConfig = JSON.parse(readFileSync(SERVER_WRANGLER, 'utf-8'))
  wranglerConfig.main = '../cron-entry.js'
  wranglerConfig.no_bundle = false
  wranglerConfig.rules = [
    { type: 'ESModule', globs: ['**/*.js', '**/*.mjs', '**/*.ts'] },
  ]
  writeFileSync(SERVER_WRANGLER, JSON.stringify(wranglerConfig, null, 2))
  console.log('✅ Patched dist/server/wrangler.json main -> ../cron-entry.js')
}
