// scripts/create-cron-entry.mjs
// post-build：產生 dist/cron-entry.js，讓 wrangler 用這個 entry，
// 同時保留 Astro 的 fetch handler 並加入 scheduled handler
// 也更新 dist/server/wrangler.json 指向 cron-entry.js

import { writeFileSync, readFileSync, existsSync } from 'node:fs'

const ASTRO_WORKER = './dist/server/entry.mjs'
const OUTPUT = './dist/cron-entry.js'
const SERVER_WRANGLER = './dist/server/wrangler.json'

if (!existsSync(ASTRO_WORKER)) {
  console.error('Astro worker not found. Run astro build first.')
  process.exit(1)
}

const content = `// Auto-generated by scripts/create-cron-entry.mjs
// Do not edit manually.
import astroWorker from './server/entry.mjs';

export default {
  // 保留 Astro 的所有 handlers
  ...astroWorker,

  // 加入 Cron scheduled handler
  async scheduled(event, env, ctx) {
    const secret = env.CRAWL_SECRET
    if (!secret) {
      console.error('[cron] CRAWL_SECRET not set, skipping crawl sync')
      return
    }

    console.log('[cron] Triggering crawl sync...')
    ctx.waitUntil(
      fetch('https://quidproquo.cc/api/crawl/sync', {
        method: 'POST',
        headers: {
          'X-Crawl-Secret': secret,
          'Content-Type': 'application/json',
        },
      }).then(async (res) => {
        const body = await res.json()
        console.log('[cron] Crawl sync result:', res.status, JSON.stringify(body))
      }).catch((err) => {
        console.error('[cron] Crawl sync failed:', err)
      })
    )
  },
}
`

writeFileSync(OUTPUT, content)
console.log('✅ Created', OUTPUT)

// Patch dist/server/wrangler.json to point main to cron-entry.js
if (existsSync(SERVER_WRANGLER)) {
  const wranglerConfig = JSON.parse(readFileSync(SERVER_WRANGLER, 'utf-8'))
  wranglerConfig.main = '../cron-entry.js'
  writeFileSync(SERVER_WRANGLER, JSON.stringify(wranglerConfig, null, 2))
  console.log('✅ Patched dist/server/wrangler.json main -> ../cron-entry.js')
}
