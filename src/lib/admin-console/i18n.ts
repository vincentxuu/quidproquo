const dictionaries = { 'zh-TW': zh, en }

export type ConsoleI18nKey = keyof typeof zh

export function getConsoleLang(url: URL | string): ConsoleLang {
  const parsed = typeof url === 'string' ? new URL(url, 'https://quidproquo.cc') : url
  const requested = parsed.searchParams.get('lang')
  return requested === 'en' ? 'en' : 'zh-TW'
}

export function consoleHref(path: string, lang: ConsoleLang): string {
  if (lang === 'zh-TW') return path
  const url = new URL(path, 'https://quidproquo.cc')
  url.searchParams.set('lang', lang)
  return `${url.pathname}${url.search}${url.hash}`
}

export function switchConsoleLangHref(url: URL, targetLang: ConsoleLang): string {
  const next = new URL(url.toString())
  if (targetLang === 'zh-TW') {
    next.searchParams.delete('lang')
  } else {
    next.searchParams.set('lang', targetLang)
  }
  return `${next.pathname}${next.search}${next.hash}`
}

export function createConsoleTranslator(lang: ConsoleLang) {
  const dict = dictionaries[lang]
  return (key: ConsoleI18nKey, vars: Record<string, string | number> = {}) => {
    const template = dict[key] ?? zh[key] ?? key
    return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`))
  }
}
